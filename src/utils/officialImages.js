// 获取图片的工具函数
export function getOfficialImages(category = null) {
  return category ? officialImages[category] || [] : officialImages
}

export function getOfficialImage(category, id) {
  const images = getOfficialImages(category)
  return images.find(img => img.id === id)
}

export function getRandomCarouselImages(count = 4) {
  const shuffled = [...officialImages.carousel].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

export default officialImages